


def insert_sort(array):
    for i in range(1,len(array)):
        key = array[i]
        for j in range(i-1,-1,-1):
            while (j >= 0 and array[j] > key ):
                tmp = array[j+1]
                array[j+1] = array[j]
                array[j] = tmp
                j = j - 1
            array[j+1] = key
            break
    return array


